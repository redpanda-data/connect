package icebergx

import (
	"errors"
	"fmt"
	"slices"

	"github.com/apache/iceberg-go"
)

// FieldAddition represents a field to add at a specific path.
type FieldAddition struct {
	Path Path
	Type iceberg.Type
}

// MakeSchemaFieldOptional takes a schema and a path, makes the field at that path optional
// (Required: false), and returns a new schema. The original schema is not modified.
func MakeSchemaFieldOptional(schema *iceberg.Schema, path Path) (*iceberg.Schema, error) {
	if schema == nil {
		return nil, errors.New("schema cannot be nil")
	}
	if len(path) == 0 {
		return nil, errors.New("path cannot be empty")
	}

	fields := copyFields(schema.Fields())
	if err := modifyFieldAtPath(fields, path, func(f *iceberg.NestedField) {
		f.Required = false
	}); err != nil {
		return nil, err
	}

	return iceberg.NewSchemaWithIdentifiers(
		schema.ID,
		schema.IdentifierFieldIDs,
		fields...,
	), nil
}

// AddFieldsToSchema takes a schema and a slice of FieldAdditions, creating fields at
// the given paths with mkdir -p semantics (creating intermediate structs as needed).
// Field IDs are auto-generated by incrementing from the max existing field ID.
// Intermediate structs are created as optional (Required: false).
// The original schema is not modified.
func AddFieldsToSchema(schema *iceberg.Schema, additions []FieldAddition) (*iceberg.Schema, error) {
	if schema == nil {
		return nil, errors.New("schema cannot be nil")
	}
	if len(additions) == 0 {
		return schema, nil
	}

	maxID := slices.Max(schema.FieldIDs())
	nextID := func() int {
		maxID++
		return maxID
	}

	fields := copyFields(schema.Fields())

	for _, addition := range additions {
		if len(addition.Path) == 0 {
			return nil, errors.New("path cannot be empty")
		}
		if addition.Type == nil {
			return nil, fmt.Errorf("type cannot be nil for path %v", addition.Path)
		}

		var err error
		fields, err = ensurePathExists(fields, addition.Path, addition.Type, nextID)
		if err != nil {
			return nil, fmt.Errorf("adding field at path %v: %w", addition.Path, err)
		}
	}

	return iceberg.NewSchemaWithIdentifiers(
		schema.ID,
		schema.IdentifierFieldIDs,
		fields...,
	), nil
}

// copyFields creates a deep copy of a slice of NestedFields.
func copyFields(fields []iceberg.NestedField) []iceberg.NestedField {
	if fields == nil {
		return nil
	}
	result := make([]iceberg.NestedField, len(fields))
	for i, f := range fields {
		result[i] = iceberg.NestedField{
			ID:       f.ID,
			Name:     f.Name,
			Required: f.Required,
			Type:     copyType(f.Type),
			Doc:      f.Doc,
		}
	}
	return result
}

// copyType creates a deep copy of an iceberg.Type.
func copyType(t iceberg.Type) iceberg.Type {
	switch typ := t.(type) {
	case *iceberg.StructType:
		return &iceberg.StructType{
			FieldList: copyFields(typ.Fields()),
		}
	case *iceberg.ListType:
		return &iceberg.ListType{
			ElementID:       typ.ElementID,
			Element:         copyType(typ.Element),
			ElementRequired: typ.ElementRequired,
		}
	case *iceberg.MapType:
		return &iceberg.MapType{
			KeyID:         typ.KeyID,
			KeyType:       copyType(typ.KeyType),
			ValueID:       typ.ValueID,
			ValueType:     copyType(typ.ValueType),
			ValueRequired: typ.ValueRequired,
		}
	default:
		// Primitive types are immutable, return as-is.
		return t
	}
}

// modifyFieldAtPath navigates to a field at the given path and applies a modification.
func modifyFieldAtPath(fields []iceberg.NestedField, path Path, modify func(*iceberg.NestedField)) error {
	if len(path) == 0 {
		return errors.New("path cannot be empty")
	}

	seg := path[0]
	remaining := path[1:]

	switch seg.Kind {
	case PathField:
		for i := range fields {
			if fields[i].Name == seg.Name {
				if len(remaining) == 0 {
					// Found the target field.
					modify(&fields[i])
					return nil
				}
				// Navigate deeper.
				return modifyTypeAtPath(fields[i].Type, remaining, modify)
			}
		}
		return fmt.Errorf("field %q not found", seg.Name)

	default:
		return fmt.Errorf("path must start with a field segment, got %v", seg.Kind)
	}
}

// modifyTypeAtPath navigates into a type and modifies a field at the remaining path.
func modifyTypeAtPath(t iceberg.Type, path Path, modify func(*iceberg.NestedField)) error {
	if len(path) == 0 {
		return errors.New("unexpected empty path in modifyTypeAtPath")
	}

	seg := path[0]
	remaining := path[1:]

	switch typ := t.(type) {
	case *iceberg.StructType:
		if seg.Kind != PathField {
			return fmt.Errorf("expected field segment in struct, got %v", seg.Kind)
		}
		for i := range typ.FieldList {
			if typ.FieldList[i].Name == seg.Name {
				if len(remaining) == 0 {
					modify(&typ.FieldList[i])
					return nil
				}
				return modifyTypeAtPath(typ.FieldList[i].Type, remaining, modify)
			}
		}
		return fmt.Errorf("field %q not found in struct", seg.Name)

	case *iceberg.ListType:
		if seg.Kind != PathListElement {
			return fmt.Errorf("expected list element segment [*], got %v", seg.Kind)
		}
		if len(remaining) == 0 {
			return errors.New("cannot modify list element directly, must specify a field within")
		}
		return modifyTypeAtPath(typ.Element, remaining, modify)

	case *iceberg.MapType:
		if seg.Kind != PathMapEntry {
			return fmt.Errorf("expected map entry segment {}, got %v", seg.Kind)
		}
		if len(remaining) == 0 {
			return errors.New("cannot modify map entry directly, must specify a field within")
		}
		return modifyTypeAtPath(typ.ValueType, remaining, modify)

	default:
		return fmt.Errorf("cannot navigate into primitive type %T", t)
	}
}

// ensurePathExists creates all intermediate structs needed and adds the final field.
func ensurePathExists(fields []iceberg.NestedField, path Path, finalType iceberg.Type, nextID func() int) ([]iceberg.NestedField, error) {
	if len(path) == 0 {
		return nil, errors.New("path cannot be empty")
	}

	seg := path[0]
	remaining := path[1:]

	if seg.Kind != PathField {
		return nil, fmt.Errorf("path must start with a field segment, got %v", seg.Kind)
	}

	// Find or create the field.
	idx := -1
	for i := range fields {
		if fields[i].Name == seg.Name {
			idx = i
			break
		}
	}

	if len(remaining) == 0 {
		// This is the final segment - add the field.
		if idx >= 0 {
			return nil, fmt.Errorf("field %q already exists", seg.Name)
		}
		newField := iceberg.NestedField{
			ID:       nextID(),
			Name:     seg.Name,
			Type:     finalType,
			Required: false, // New fields are optional.
		}
		return append(fields, newField), nil
	}

	// Need to navigate deeper.
	if idx < 0 {
		// Create intermediate struct.
		newStruct := &iceberg.StructType{FieldList: nil}
		newField := iceberg.NestedField{
			ID:       nextID(),
			Name:     seg.Name,
			Type:     newStruct,
			Required: false, // Intermediate structs are optional.
		}
		fields = append(fields, newField)
		idx = len(fields) - 1
	}

	// Navigate into the field's type.
	newType, err := ensurePathInType(fields[idx].Type, remaining, finalType, nextID)
	if err != nil {
		return nil, err
	}
	fields[idx].Type = newType

	return fields, nil
}

// ensurePathInType creates path within a type and returns the modified type.
func ensurePathInType(t iceberg.Type, path Path, finalType iceberg.Type, nextID func() int) (iceberg.Type, error) {
	if len(path) == 0 {
		return nil, errors.New("unexpected empty path in ensurePathInType")
	}

	seg := path[0]
	remaining := path[1:]

	switch typ := t.(type) {
	case *iceberg.StructType:
		if seg.Kind != PathField {
			return nil, fmt.Errorf("expected field segment in struct, got %v", seg.Kind)
		}
		newFields, err := ensurePathExists(typ.FieldList, path, finalType, nextID)
		if err != nil {
			return nil, err
		}
		return &iceberg.StructType{FieldList: newFields}, nil

	case *iceberg.ListType:
		if seg.Kind != PathListElement {
			return nil, fmt.Errorf("expected list element segment [*], got %v", seg.Kind)
		}
		if len(remaining) == 0 {
			return nil, errors.New("cannot add field directly to list element, must specify path within element")
		}
		newElement, err := ensurePathInType(typ.Element, remaining, finalType, nextID)
		if err != nil {
			return nil, err
		}
		return &iceberg.ListType{
			ElementID:       typ.ElementID,
			Element:         newElement,
			ElementRequired: typ.ElementRequired,
		}, nil

	case *iceberg.MapType:
		if seg.Kind != PathMapEntry {
			return nil, fmt.Errorf("expected map entry segment {}, got %v", seg.Kind)
		}
		if len(remaining) == 0 {
			return nil, errors.New("cannot add field directly to map entry, must specify path within value")
		}
		newValue, err := ensurePathInType(typ.ValueType, remaining, finalType, nextID)
		if err != nil {
			return nil, err
		}
		return &iceberg.MapType{
			KeyID:         typ.KeyID,
			KeyType:       typ.KeyType,
			ValueID:       typ.ValueID,
			ValueType:     newValue,
			ValueRequired: typ.ValueRequired,
		}, nil

	default:
		return nil, fmt.Errorf("cannot create path through primitive type %T", t)
	}
}
